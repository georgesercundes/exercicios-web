// Maneira antiga 
const exp = Math.pow(2,3) // O primeiro parâmetro é a base e o segundo o expoente

console.log(exp) // 2 elevado a 3 igual 8

// Maneira nova
const exp2 = 2 ** 3 // O primeiro valor representa a a base e o segundo o expoente

console.log(exp2) // 2 elevado a 3 igual 8

let base = 2
base **= 3 // Eleva o valor armazenado na base à terceira potência

console.log(base)// 2 elevado a 3 igual a 8